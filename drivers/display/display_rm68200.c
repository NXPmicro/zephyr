static struct rm68200_data rm68200_data_0;

#define PANEL_PIN_BINDING(drv_data, pin_type) \
	{ \
		char *pin_name = DT_INST_GPIO_LABEL(0, pin_type ##_gpios); \
		drv_data->pin_type ##_pin = DT_INST_GPIO_PIN(0, pin_type ##_gpios); \
		drv_data->pin_type ##_flags = DT_INST_GPIO_FLAGS(0, pin_type ##_gpios); \
		if (pin_name) { \
			drv_data->pin_type ##_gpio = device_get_binding(pin_name); \
			if (drv_data->pin_type ##_gpio == NULL) { \
				LOG_ERR("Failed to get pointer to %s device!", \
					pin_name); \
			} \
		} \
	}

static int rm68200_init_0(const struct device *dev)
{
	struct rm68200_data *drv_data = dev->data;

	PANEL_PIN_BINDING(drv_data, reset);
	PANEL_PIN_BINDING(drv_data, pwr);
	PANEL_PIN_BINDING(drv_data, bl);

	drv_data->mipi_dsi = device_get_binding(DT_INST_BUS_LABEL(0));
	if (drv_data->mipi_dsi == NULL) {
		LOG_ERR("Failed to get pointer to %s device!",
			DT_INST_LABEL(0));
		return -EINVAL;
	}

	return rm68200_init(dev);
}

