extern int mipi_dsi_dcs_write(const struct device *dev, const uint8_t *buf, uint8_t len);
static int rm68200_generic_write(const struct device *dev, uint8_t *buf,
			    uint8_t len)
{
	struct rm68200_data *drv_data = dev->data;

	return mipi_dsi_dcs_write(drv_data->mipi_dsi, buf, len);
}

static int rm68200_write_all(const struct device *dev,
			    const uint8_t buf[][2],
			    uint16_t reg_num)
{
	uint16_t i = 0;

	for (i = 0; i < reg_num; i++) {
		int err;

		err = rm68200_generic_write(dev, (uint8_t *)buf[i], 2);
		if (err) {
			return err;
		}
	}

	return 0;
}

static int rm68200_init(const struct device *dev)
{
	struct rm68200_data *drv_data = dev->data;
	uint8_t param[2];
	int ret;

	if (drv_data->reset_gpio && drv_data->pwr_gpio) {
		ret = gpio_pin_configure(drv_data->reset_gpio,
					 drv_data->reset_pin,
					 GPIO_OUTPUT_INACTIVE |
					 drv_data->reset_flags);
		if (ret) {
			return ret;
		}
		ret = gpio_pin_configure(drv_data->pwr_gpio,
					 drv_data->pwr_pin,
					 GPIO_OUTPUT_INACTIVE |
					 drv_data->pwr_flags);
		if (ret) {
			return ret;
		}
		ret = gpio_pin_configure(drv_data->bl_gpio,
					 drv_data->bl_pin,
					 GPIO_OUTPUT_INACTIVE |
					 drv_data->bl_flags);
		if (ret) {
			return ret;
		}

		/* pwr:1 -> reset:0 -> reset:1 */
		gpio_pin_set(drv_data->pwr_gpio,
			     drv_data->pwr_pin,
			     1);
		k_sleep(K_MSEC(1));
		gpio_pin_set(drv_data->reset_gpio,
			     drv_data->reset_pin,
			     0);
		k_sleep(K_MSEC(1));
		gpio_pin_set(drv_data->reset_gpio,
			     drv_data->reset_pin,
			     1);
		k_sleep(K_MSEC(5));
	}

	ret = rm68200_write_all(dev, rm68200InitParam, ARRAY_SIZE(rm68200InitParam));
	if (ret) {
		LOG_ERR("Failed to init the reg of the rm68200");
		return -EIO;
	}

	k_sleep(K_MSEC(200));

	param[0] = 0x29;
	param[1] = 0x00;
	rm68200_generic_write(dev, param, 2);

	k_sleep(K_MSEC(100));

	param[0] = 0x2c;
	rm68200_generic_write(dev, param, 1);

	param[0] = 0x35;
	param[1] = 0x00;
	rm68200_generic_write(dev, param, 2);

	k_sleep(K_MSEC(200));

	/* turn the backlight on */
	if (drv_data->bl_gpio) {
		ret = gpio_pin_set(drv_data->bl_gpio,
					 drv_data->bl_pin,
					 1);
		if (ret) {
			return ret;
		}
	}

	return 0;
}

